package Homework1;

public class Bitwise {
public static void main(String[] args) {
	int a,b,c;
	a=33;
	b=12;
	c=a|b;
	System.out.println("Answer a|b = "+c);
	
	int e,f,g;
	e=11;
	f=33;
	g=e&f;
	System.out.println("Ans e&f = "+g);
	
	int q,w,s;
	q=14;
	w=77;
	s=q^w;
	System.out.println("Answer q^w = "+s);
	
	int z,x;
	z=10;
	x=~z;
	System.out.println("Answer ~z = "+x);
	
	
			
}
}
